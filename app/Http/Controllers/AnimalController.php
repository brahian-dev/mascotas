    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $animal = new Animal();
            $animal->animalName = $request->name;
            $animal->animalColor = $request->color;
            $animal->colorcast_colorcastId = $request->colorcast;
            $animal->gender_genderId = $request->gender;
            $animal->state_stateId = State::getState('available');
            $animal->save();

            return back()->with('successAnimal', 'Insertado correctamente');
        } catch (Exception $e) {
            return back()->withErrors([
                'message' => $e
            ]);
        }
    }

